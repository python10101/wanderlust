# Marks to grade conversion

a=eval(input("Enter your marks: "))
print("Your grade is: ")
if a>90:
            print("O")
elif a>80 and a<90:
            print("A")
elif a>70 and a<80:
            print("B")
elif a>60 and a<70:
            print("C")
elif a>50 and a<60:
            print("D")
elif a>40 and a<50:
            print("E")
elif a<40:
            print("F")


